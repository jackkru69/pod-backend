func loadEmittedEventTimestamp(slice *cell.Slice, eventName string) error {
	if _, err := slice.LoadUInt(32); err != nil {
		return fmt.Errorf("%s: failed to read timestamp: %w", eventName, err)
	}

	return nil
}

